export function getCronPatternBySlug(slug: string): CronPattern | undefined {
	return cronPatterns.find((p) => p.slug === slug)
}

export function getRelatedPatterns(pattern: CronPattern): CronPattern[] {
	return pattern.relatedSlugs
		.map((slug) => getCronPatternBySlug(slug))
		.filter((p): p is CronPattern => p !== undefined)
}

export function getPatternsByCategory(
	category: CronPattern["category"],
): CronPattern[] {
	return cronPatterns.filter((p) => p.category === category)
}
